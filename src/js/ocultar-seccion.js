function mostrarSeccion() {
  document.getElementById("b-cont2").style.display = "flex";
  document.getElementById("saber-mas-cont").style.display = "none";
  document.getElementById("saber-men-cont").style.display = "flex";
}

function ocultarSeccion() {
  document.getElementById("b-cont2").style.display = "none";
  document.getElementById("saber-mas-cont").style.display = "flex";
  document.getElementById("saber-men-cont").style.display = "none";
}
